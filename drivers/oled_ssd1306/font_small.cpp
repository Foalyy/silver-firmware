    Char5 getSmall(char c) {
        unsigned int i = (int)c - 32;
        if (i >= 0 && i <= N_CHARS_TOTAL) {
            return fontSmall[mapSmall[i]];
        } else {
            return fontSmall[0];
        }
    }

